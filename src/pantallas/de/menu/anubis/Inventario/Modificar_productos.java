    public void modificar_productos(){
        try {
            if(cambiar_imagen == 0){
                System.out.println(jTextField1.getText());
                conexion.bd cbd = new bd();
                cbd.setServidor("jdbc:mysql://localhost/sistema_pos_anubis");
                cbd.setUsuario("root");
                cbd.setContrasena("");
                Connection con = cbd.getConexion();
                PreparedStatement pst = con.prepareStatement("UPDATE inventario SET nombre=?,cantidad=?,precio=? WHERE id = '"+jTextField1.getText()+"'");
                pst.setString(1, jTextField3.getText());
                int cantidad = (int) jSpinner1.getValue();
                pst.setString(2, Integer.toString(cantidad));
                pst.setString(3, jTextField2.getText());
                pst.executeUpdate();
                mod_dat_productos_val = 1;
                this.dispose();
            }else if(cambiar_imagen==1){
                conexion.bd cbd = new bd();
                cbd.setServidor("jdbc:mysql://localhost/sistema_pos_anubis");
                cbd.setUsuario("root");
                cbd.setContrasena("");
                Connection con = cbd.getConexion();
                PreparedStatement pst = con.prepareStatement("UPDATE inventario SET nombre=?,cantidad=?,imagen=?,precio=? WHERE id = '"+jTextField1.getText()+"'");
                pst.setString(1, jTextField3.getText());
                int cantidad = (int) jSpinner1.getValue();
                pst.setString(2, Integer.toString(cantidad));
                pst.setBlob(3, ruta, this.longitud_bytes);
                pst.setString(4, jTextField2.getText());
                pst.executeUpdate();
                mod_dat_productos_val = 1;
                cambiar_imagen = 0;
                jLabel8.setText("");
                this.dispose();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(modificar_empleados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

