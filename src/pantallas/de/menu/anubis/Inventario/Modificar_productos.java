    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser j = new JFileChooser();
        int ap = j.showOpenDialog(this);
        if(ap == JFileChooser.APPROVE_OPTION){
            try {
                ruta = new FileInputStream(j.getSelectedFile());
                this.longitud_bytes = (int) j.getSelectedFile().length();
                try {
                    Image icono = ImageIO.read(j.getSelectedFile()).getScaledInstance(jLabel5.getWidth(), jLabel5.getHeight(), Image.SCALE_DEFAULT);
                    jLabel5.setIcon(new ImageIcon(icono));
                    jLabel5.updateUI();
                    jLabel8.setText(j.getSelectedFile().getAbsolutePath());
                    cambiar_imagen = 1;
                } catch (IOException ex) {
                    Logger.getLogger(Registrar_productos.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Registrar_productos.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

